    // for attendance report generating
    public ArrayList<Attendance>attendanceList(){
    
        ArrayList<Attendance>attendent_list = new ArrayList<>();
        
        try{
            ConnectionDB c = new ConnectionDB();
            Statement stmt =c.getConnectionDB().createStatement();
            String Stu_id=Stu_Id.getText();
            String query="SELECT FName,LName,NIC,Date FROM attendance WHERE SID='"+Stu_id+"'" ;
            ResultSet rs= stmt.executeQuery(query);
            
            while(rs.next()){
            Attendance attendance= new Attendance(rs.getString("FName"),rs.getString("LName"),rs.getString("NIC"),rs.getDate("Date"));
            attendent_list.add(attendance);
            
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        
        }
        return attendent_list;
    }
    
     public void show_attendentlist(){
    ArrayList<Attendance> list= attendanceList();
        DefaultTableModel model=(DefaultTableModel)attendance_table.getModel();
        Object[] row = new Object[4];
        
        for(int i=0;i<list.size();i++){
            
            row[0]=list.get(i).getFname();
            row[1]=list.get(i).getLname();
            row[2]=list.get(i).getNIC();
            row[3]=list.get(i).getDate();
            model.addRow(row);
        }
    }
    
    
    // for Mark's report generating
    public ArrayList<Mark>subjectList(){
        ArrayList<Mark> marklist= new ArrayList<>();
        try{
       ConnectionDB conn=new ConnectionDB();
       
       Statement stmt = conn.getConnectionDB().createStatement();
       
       String mark_subID=SubName.getText();
       
       String query="SELECT MStuId,MpaperNo,MarkObtain FROM mark WHERE MSubject='"+mark_subID+"'";
       
       ResultSet rs= stmt.executeQuery(query);
       while(rs.next()){
       Mark mark = new Mark(rs.getString("MStuId"),rs.getString("MPaperNo"),rs.getString("MarkObtain"));
       marklist.add(mark);
       }
        }catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
        return marklist;
    }
        
