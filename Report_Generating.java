    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       
        
    
        
        
        try {
            
            Document doc=new Document();
            try {
                
                File f=new File("Report2.pdf");
                if(!f.exists()){
                    try {
                        f.createNewFile();
                    } catch (IOException ex) {
                        Logger.getLogger(Report_Generating.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
                
                
                PdfWriter.getInstance(doc,new FileOutputStream(f));
            } catch (DocumentException ex) {
                Logger.getLogger(Report_Generating.class.getName()).log(Level.SEVERE, null, ex);
            }
            doc.open();
            
             doc.add(new Paragraph("Student ID"+"   |   "+"Paper No"+"   |    "+"Marks Obtain"));
            try {
                
                
                for (int i = 0; i < marks_table.getSelectedRowCount(); i++) {
                    
                    doc.add(new Paragraph(marks_table.getValueAt(i, 0).toString()+"                    "+marks_table.getValueAt(i, 1).toString()+"                      "+marks_table.getValueAt(i, 2).toString()));
               
                
                }
                
                
            } catch (DocumentException ex) {
                Logger.getLogger(Report_Generating.class.getName()).log(Level.SEVERE, null, ex);
            }
            doc.close();
        } catch (FileNotFoundException ex) {
            Logger.getLogger(Report_Generating.class.getName()).log(Level.SEVERE, null, ex);
        } catch (DocumentException ex) {
            Logger.getLogger(Report_Generating.class.getName()).log(Level.SEVERE, null, ex);
        }
    
      
    }//GEN-LAST:event_jButton1ActionPerformed

    private void PDF_Gene_BtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_PDF_Gene_BtnActionPerformed
     Document doc= new Document();
     try{
     String path="";
     String Stu_id=Stu_Id.getText();
     JFileChooser j= new JFileChooser();
     j.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
     int x=j.showSaveDialog(this);
     
     if(x==JFileChooser.APPROVE_OPTION){
         path=j.getSelectedFile().getPath();
      
     }
     
     PdfWriter.getInstance(doc,new FileOutputStream(path+Stu_id+".pdf"));
     
     doc.open();
         doc.add(new Paragraph("Attendance Report                "));
         
         PdfPTable tbl= new PdfPTable(4);
         
         //Adding headers
         tbl.addCell("First name");
         tbl.addCell("Last name");
         tbl.addCell("NIC");
         tbl.addCell("Date");
         
         for(int i =0; i< attendance_table.getRowCount();i++){
             String Fname=attendance_table.getValueAt(i, 0).toString();
             String Lname=attendance_table.getValueAt(i, 1).toString();
             String NIC=attendance_table.getValueAt(i, 2).toString();
             String date=attendance_table.getValueAt(i, 3).toString();
             tbl.addCell(Fname);
             tbl.addCell(Lname);
             tbl.addCell(NIC);
             tbl.addCell(date);
         
         }
         doc.add(tbl);
         JOptionPane.showMessageDialog(this,"Successfully Created");
         
         
     }catch(Exception e){
         JOptionPane.showMessageDialog(rootPane, e.getMessage());
     
     } 
     doc.close();
    }//GEN-LAST:event_PDF_Gene_BtnActionPerformed

